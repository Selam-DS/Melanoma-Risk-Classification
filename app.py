import gradio as gr
import torch
import torchvision.models as models
import torchvision.transforms as transforms

import pandas as pd
import numpy as np
import joblib

from PIL import Image

# ===================================
# LOAD SAVED PIPELINE ARTIFACTS
# ===================================

bundle = joblib.load("melanoma_pipeline.pkl")

model = bundle["model"]
scaler = bundle["scaler"]
selector = bundle["selector"]
encoder = bundle["encoder"]
threshold = bundle["threshold"]

encoded_feature_order = joblib.load(
    "encoded_feature_order.pkl"
)

# ===================================
# DEVICE SETUP
# ===================================

device = torch.device("cpu")

# ===================================
# RESNET18 FEATURE EXTRACTOR
# ===================================

resnet = models.resnet18(weights="DEFAULT")

# Remove classification layer
resnet = torch.nn.Sequential(
    *list(resnet.children())[:-1]
)

resnet.eval()
resnet.to(device)

# ===================================
# IMAGE TRANSFORMATIONS
# ===================================

transform = transforms.Compose([
    transforms.Resize((224, 224)),
    transforms.ToTensor(),
    transforms.Normalize(
        mean=[0.485, 0.456, 0.406],
        std=[0.229, 0.224, 0.225]
    )
])

# ===================================
# PREDICTION FUNCTION
# ===================================

def predict(image, age, sex, site):

    try:

        # ---------------------------
        # VALIDATE IMAGE
        # ---------------------------

        if image is None:
            return "Please upload an image."

        # ---------------------------
        # IMAGE FEATURES
        # ---------------------------

        img = transform(image).unsqueeze(0).to(device)

        with torch.no_grad():

            img_features = resnet(img)

            img_features = img_features.view(
                img_features.size(0),
                -1
            )

            img_features = img_features.cpu().numpy()

        # ---------------------------
        # METADATA
        # ---------------------------

        metadata = pd.DataFrame([{
            "sex": sex,
            "age_approx": age,
            "anatom_site_general_challenge": site
        }])

        encoded_meta = encoder.transform(metadata)

        # Convert sparse matrix if needed
        if hasattr(encoded_meta, "toarray"):
            encoded_meta = encoded_meta.toarray()

        encoded_meta = pd.DataFrame(
            encoded_meta,
            columns=encoded_feature_order
        )

        # ---------------------------
        # COMBINE FEATURES
        # ---------------------------

        X = np.hstack([
            img_features,
            encoded_meta.values
        ])

        # ---------------------------
        # FEATURE SELECTION
        # ---------------------------

        X = selector.transform(X)

        # ---------------------------
        # SCALING
        # ---------------------------

        X = scaler.transform(X)

        # ---------------------------
        # PREDICTION
        # ---------------------------

        probability = model.predict_proba(X)[0, 1]

        prediction = (
            "Higher Melanoma Risk"
            if probability > threshold
            else "Lower Melanoma Risk"
        )

        probability = round(
            float(probability),
            4
        )

        # ---------------------------
        # RETURN RESULT
        # ---------------------------

        return (
            f"Prediction: {prediction}\n\n"
            f"Melanoma Probability: {probability}"
        )

    except Exception as e:

        return f"Error: {str(e)}"

# ===================================
# GRADIO INTERFACE
# ===================================

demo = gr.Interface(
    fn=predict,

    inputs=[

        gr.Image(
            type="pil",
            label="Upload Skin Lesion Image"
        ),

        gr.Slider(
            minimum=0,
            maximum=100,
            value=45,
            step=1,
            label="Age"
        ),

        gr.Dropdown(
            choices=[
                "male",
                "female",
                "unknown"
            ],
            value="unknown",
            label="Sex"
        ),

        gr.Dropdown(
            choices=[
                "torso",
                "lower extremity",
                "upper extremity",
                "head/neck",
                "unknown"
            ],
            value="unknown",
            label="Anatomical Site"
        )
    ],

    outputs=gr.Textbox(
        label="Prediction Result"
    ),

    title="Melanoma Risk Assessment",

    description="""
AI-powered melanoma risk assessment using:
• ResNet18 image embeddings
• Clinical metadata
• Logistic Regression

Educational research prototype only.
Not intended for clinical diagnosis.
"""
)

# ===================================
# LAUNCH APPLICATION
# ===================================

demo.launch(
    share=True,
    show_api=False
)